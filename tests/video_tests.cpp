void print_buff(uint8_t* buffer, int w, int h, int pitch, int depth)
{
    char str[pitch];
    int x;
    for (int y = 0; y < h; y++)
    {
        for (x = 0; x < w*depth; x++)
        {
            printf("%d", buffer[y*pitch + x]);
        }
        printf("\n");
    }
}

